let dolphinsaverage1 = (96 + 108 + 89) / 3
let koalasaverage1 = (88 + 91 + 110) / 3
console.log(dolphinsaverage1, koalasaverage1)
if (dolphinsaverage1 > koalasaverage1 && dolphinsaverage1 > 100) {
    console.log("dolphin have won")
}
else if (koalasaverage1 > dolphinsaverage1 && koalasaverage1 > 100) {
    console.log("koalas have won")
} else if (dolphinsaverage1 > 100 && dolphinsaverage1 === koalasaverage1) {
    console.log("Its a tie")
} else{
    console.log("No teams have won!")
}

